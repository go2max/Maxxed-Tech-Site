import assert from "node:assert/strict";
import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import { extname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";

import { apps, wordpressPlugins } from "../content/site-data.mjs";
import { powerhouseProducts, repoProducts } from "../content/repo-products.mjs";

const root = resolve(fileURLToPath(new URL("..", import.meta.url)));
const siteRoot = resolve(root, "site");
const reportsRoot = resolve(root, "reports");
const publicProducts = [...apps, ...wordpressPlugins, ...repoProducts, ...powerhouseProducts];
const expectedGuidePaths = [
  "/android-utility-apps/",
  "/wordpress-cleanup-plugins/",
  "/camera-measurement-apps/",
  "/compass-outdoor-tools/",
  "/beta-testing-android-apps/",
];
const bannedPublicPatterns = [
  /release candidate/i,
  /Release verification/i,
  /Internal testing/i,
  /Active development/i,
  /repo-backed/i,
  /repo products/i,
  /Powerhouse repo/i,
  /plugin lab/i,
  /beta@techmaxxed\.com/i,
  /privacy@techmaxxed\.com/i,
  /href="(?:https:\/\/techmaxxed\.com)?\/admin(?:\/|")/i,
  /href="\.\.\/admin(?:\/|")/i,
  /href="\.\.\/\.\.\/admin(?:\/|")/i,
];

async function filesUnder(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const path = join(directory, entry.name);
    if (entry.isDirectory()) files.push(...await filesUnder(path));
    else files.push(path);
  }
  return files;
}

const files = await filesUnder(siteRoot);
const htmlFiles = files.filter((file) => extname(file) === ".html");
const routeFor = (file) => {
  const path = `/${relative(siteRoot, file).split(sep).join("/")}`;
  return path.endsWith("/index.html") ? path.slice(0, -"index.html".length) : path;
};
const htmlByRoute = new Map();
for (const file of htmlFiles) htmlByRoute.set(routeFor(file), await readFile(file, "utf8"));

const sitemap = await readFile(resolve(siteRoot, "sitemap.xml"), "utf8");
const sitemapUrls = [...sitemap.matchAll(/<loc>https:\/\/techmaxxed\.com\/([^<]*)<\/loc>/g)]
  .map((match) => `/${match[1]}`);

const missingGuidePaths = expectedGuidePaths.filter((path) => !htmlByRoute.has(path));
const toolPages = [...htmlByRoute.keys()].filter((path) => path.startsWith("/tools/") && path.endsWith("/"));
const productSupportMissing = publicProducts.filter((product) => !htmlByRoute.get("/support/")?.includes(`<option value="${product.name.replaceAll("&", "&amp;").replaceAll('"', "&quot;")}">`));
const bannedFindings = [];
for (const [route, html] of htmlByRoute) {
  if (route.startsWith("/admin/")) continue;
  for (const pattern of bannedPublicPatterns) {
    if (pattern.test(html)) bannedFindings.push({ route, pattern: String(pattern) });
  }
}

const summary = {
  htmlPages: htmlFiles.length,
  sitemapUrls: sitemapUrls.length,
  androidApps: apps.length,
  wordpressPlugins: wordpressPlugins.length,
  focusedWebTools: repoProducts.length,
  businessTools: powerhouseProducts.length,
  publicProducts: publicProducts.length,
  publicToolPages: toolPages.length,
  seoGuidePages: expectedGuidePaths.length,
  missingGuidePaths,
  productSupportMissing: productSupportMissing.map((product) => product.name),
  bannedFindings,
};

assert.equal(summary.androidApps, 6, "All six Android apps must remain present");
assert.equal(summary.wordpressPlugins, 36, "All 36 WordPress plugins must remain present");
assert.equal(summary.publicProducts, 186, "Public product catalog count changed unexpectedly");
assert.equal(summary.publicToolPages, repoProducts.length + powerhouseProducts.length, "Every focused/business tool needs a public detail page");
assert.equal(summary.missingGuidePaths.length, 0, "All SEO guide pages must exist");
assert.equal(summary.productSupportMissing.length, 0, "Every public product must be selectable from support");
assert.equal(summary.bannedFindings.length, 0, "Public pages contain blocked internal wording, old emails, or admin links");

const lines = [
  "# Public Site Readiness Report",
  "",
  "Generated by `npm run report:public` from the current built site.",
  "",
  "## Counts",
  "",
  `- Public HTML pages: ${summary.htmlPages}`,
  `- Sitemap URLs: ${summary.sitemapUrls}`,
  `- Public products: ${summary.publicProducts}`,
  `- Android apps: ${summary.androidApps}`,
  `- WordPress plugins: ${summary.wordpressPlugins}`,
  `- Focused web tools: ${summary.focusedWebTools}`,
  `- Business tools: ${summary.businessTools}`,
  `- Public tool detail pages: ${summary.publicToolPages}`,
  `- SEO guide pages: ${summary.seoGuidePages}`,
  "",
  "## Checks",
  "",
  "- All six Android apps remain present.",
  "- All 36 WordPress plugin pages remain present.",
  "- Every focused web tool and business tool has a public detail page.",
  "- Every public product is selectable from support.",
  "- Public admin links are absent from public pages.",
  "- Old beta/privacy email routes are absent from public pages.",
  "- Internal release wording scan is clean.",
  "",
  "## SEO Guide Pages",
  "",
  ...expectedGuidePaths.map((path) => `- ${path}`),
  "",
];

await mkdir(reportsRoot, { recursive: true });
await writeFile(resolve(reportsRoot, "public-site-readiness.json"), `${JSON.stringify(summary, null, 2)}\n`);
await writeFile(resolve(reportsRoot, "public-site-readiness.md"), `${lines.join("\n")}\n`);

console.log(`Public readiness passed: ${summary.htmlPages} HTML pages, ${summary.sitemapUrls} sitemap URLs, ${summary.publicProducts} products.`);
